import 'observable.dart';

/// Syntactic sugar for an [Observable] holding a `bool`.
///
/// Açúcar sintático para um [Observable] que armazena um `bool`.
typedef ObsBool = Observable<bool>;

/// Syntactic sugar for an [Observable] holding an `int`.
///
/// Açúcar sintático para um [Observable] que armazena um `int`.
typedef ObsInt = Observable<int>;

/// Syntactic sugar for an [Observable] holding a `double`.
///
/// Açúcar sintático para um [Observable] que armazena um `double`.
typedef ObsDouble = Observable<double>;

/// Syntactic sugar for an [Observable] holding a `String`.
///
/// Açúcar sintático para um [Observable] que armazena uma `String`.
typedef ObsString = Observable<String>;
